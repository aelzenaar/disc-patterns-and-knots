ResampleOperation2D.width=1600
ResampleOperation2D.height=1600

class LackenbyPurcellGroup(cayley.GroupCache):
    def __init__(self, t, with_extras=False):
        tX  = t*(0.76069 + 0.857874j)+(1.-t)*(2.- 1.j)
        tY  = t*(-0.7610 - 0.857874j)+(1.-t)*(- 1.j)
        tXY = t*(2.3146 - 2.6103j)+(1.-t)*(-2.-2.j)

        self.tX = tX
        self.tY = tY
        self.tXY = tXY

        v = mp.sqrt(4-tXY*tXY)

        X = mp.matrix([[.5*tX + .5j*tXY, - .5*tX - .5*v], [-.5*tX + .5*v, .5*tX - .5j*tXY]])
        Y = mp.matrix([[.5*tY - 1j, .5*tY], [.5*tY, .5*tY + 1j]])

        gens = [X,Y]
        names = list('XY')
        gens += [X**-1@Y@Y, X**-1@Y@Y@Y@X**-1, Y@X**-1]
        names += list('ABC')

        super().__init__(gens, names=names)



logpoints = 7

t = 1
G = LackenbyPurcellGroup(t)
limit_set_G = G.coloured_limit_set_fast(10**logpoints, seed=G.fixed_points((2,3,3,1,))[0])
print('limit set G finished')

scatter = datashade(hv.Scatter(limit_set_G, kdims = ['x'], vdims = ['y','colour']).opts(data_aspect=1, marker = "dot", size = .01)\
            .redim(x=hv.Dimension('x', range=(-2,2)),y=hv.Dimension('y', range=(-2, 2))), width=800, cmap=['black'], min_alpha=1)
hv.save(scatter.opts(width=800,data_aspect=1), f"schottky_limit_{t}.png")
